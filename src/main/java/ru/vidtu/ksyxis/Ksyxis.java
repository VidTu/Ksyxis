package ru.vidtu.ksyxis;

import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.apache.logging.log4j.Marker;
import org.apache.logging.log4j.MarkerManager;
import org.embeddedt.modernfix.core.ModernFixMixinPlugin;
import org.jetbrains.annotations.ApiStatus;
import org.jetbrains.annotations.Contract;
import org.jspecify.annotations.NullMarked;
import org.spongepowered.asm.launch.MixinBootstrap;
import org.spongepowered.asm.mixin.Mixins;
import ru.vidtu.ksyxis.platform.KCompile;
import ru.vidtu.ksyxis.platform.KPlugin;

import java.lang.reflect.Field;

/**
 * Main Ksyxis class.
 *
 * @author VidTu
 * @apiNote Internal use only
 * @see KPlugin
 */
@ApiStatus.Internal
@NullMarked
public final class Ksyxis {
    /**
     * Marker for all logs generated by the mod.
     */
    public static final Marker KSYXIS_MARKER = MarkerManager.getMarker("MOD_KSYXIS");

    /**
     * Logger for this class.
     */
    private static final Logger LOGGER = LogManager.getLogger("Ksyxis");

    /**
     * Amount of loaded chunks to report. Usually {@code 0}, {@code 441} with ModernFix.
     *
     * @see #getLoadedChunks()
     */
    // This MUST be below LOGGER, otherwise deadlocks will screw us up.
    public static final int LOADED_CHUNKS = getLoadedChunks();

    /**
     * An instance of this class cannot be created.
     *
     * @throws AssertionError Always
     * @deprecated Always throws
     */
    @ApiStatus.ScheduledForRemoval
    @Deprecated
    @Contract(value = "-> fail", pure = true)
    private Ksyxis() {
        throw new AssertionError("Ksyxis: No instances.");
    }

    /**
     * Initializes the mod. If {@code manual}, bootstraps the Mixin and injects its configuration.
     *
     * @param platform Current platform
     * @param manual   Whether to bootstrap the Mixin inject Mixin configuration manually
     * @see MixinBootstrap#init()
     * @see Mixins#addConfiguration(String)
     */
    public static void init(final String platform, final boolean manual) {
        try {
            // Log.
            final long start = System.nanoTime();
            LOGGER.info(KSYXIS_MARKER, "Ksyxis: Loading... (platform: {}, version: " + KCompile.VERSION + ", manual: {})", new Object[]{platform, manual}); // <- Array for compat with older Log4j2.

            // Get the Mixin version field.
            final Field mixinVersionField = MixinBootstrap.class.getField("VERSION");

            // Get the Mixin version field value without javac constant inlining.
            final String mixinVersion = (String) mixinVersionField.get(null);

            // Log. (**DEBUG**)
            if (KCompile.DEBUG_LOGS && LOGGER.isDebugEnabled(KSYXIS_MARKER)) {
                LOGGER.debug(KSYXIS_MARKER, "Ksyxis: Found Mixin library. (mixinVersion: {})", new Object[]{mixinVersion}); // <- Array for compat with older Log4j2.
            }

            // Do manual Mixin injections, if enabled.
            if (manual) {
                // Log. (**DEBUG**)
                if (KCompile.DEBUG_LOGS) {
                    LOGGER.debug(KSYXIS_MARKER, "Ksyxis: Bootstrapping Mixin...");
                }

                // Bootstrap Mixin.
                MixinBootstrap.init();

                // Log. (**DEBUG**)
                if (KCompile.DEBUG_LOGS) {
                    LOGGER.debug(KSYXIS_MARKER, "Ksyxis: Mixin bootstrapped. Adding the config...");
                }

                // Add the config.
                Mixins.addConfiguration("ksyxis.mixins.json");

                // Log. (**DEBUG**)
                if (KCompile.DEBUG_LOGS) {
                    LOGGER.debug(KSYXIS_MARKER, "Ksyxis: Mixin config added.");
                }
            }

            // Log the info.
            LOGGER.info(KSYXIS_MARKER, "Ksyxis: Ready to remove unneeded chunks. (mixinVersion: {}, time: {} ms)", new Object[]{mixinVersion, (System.nanoTime() - start) / 1_000_000L}); // <- Array for compat with older Log4j2.
        } catch (final Throwable t) {
            // Check if Mixin is absent.
            if ((t.getClass() == NoClassDefFoundError.class) && "org/spongepowered/asm/launch/MixinBootstrap".equals(t.getMessage())) {
                throw new RuntimeException("Ksyxis: Unexpected init error. !?!?!?!?!!?!?!?!?! HEY YOU! YES YOU! YOU PROBABLY FORGOT TO INSTALL MIXIN PROVIDER! PLEASE INSTALL IT BEFORE REPORTING ANY ISSUES! !?!?!?!?!!?!?!?!?!", t);
            }

            // Rethrow.
            throw new RuntimeException("Ksyxis: Unexpected init error.", t);
        }
    }

    /**
     * Evaluates and returns the amount of loaded chunks to report. Usually {@code 0}, because we have no spawn chunks,
     * but if ModernFix is installed, the value might be changed to {@code 441} to prevent deadlocks.
     *
     * @return Either {@code 0} or {@code 441}, depending on the configuration
     * @see #LOADED_CHUNKS
     */
    @Contract(pure = true)
    private static int getLoadedChunks() {
        try {
            // Check the removeSpawnChunks. ModernFix apparently did this too for some time, just in different way.
            final boolean removeSpawnChunks = ModernFixMixinPlugin.instance.isOptionEnabled("perf.remove_spawn_chunks.MinecraftServer");

            // Log.
            LOGGER.info(KSYXIS_MARKER, "Ksyxis: Enabled compatibility hack with ModernFix. (removeSpawnChunks: {})", new Object[]{removeSpawnChunks}); // <- Array for compat with older Log4j2.

            // Check what amount of spawn chunks to report back to the game.
            // ModernFix needs 441, because of its own way of doing it.
            // Ksyxis needs 0, because we remove all spawn chunks.
            return (removeSpawnChunks ? 441 : 0);
        } catch (final Throwable t) {
            // Log.
            if (KCompile.DEBUG_LOGS && LOGGER.isDebugEnabled(KSYXIS_MARKER)) {
                LOGGER.info(KSYXIS_MARKER, "Ksyxis: No compatibility hacks were used.", t);
            } else {
                LOGGER.info(KSYXIS_MARKER, "Ksyxis: No compatibility hacks were used.");
            }

            // No ModernFix found, it's Ksyxis only, and we have 0 chunks.
            return 0;
        }
    }
}
